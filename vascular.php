                <p><span style="font-size: 1.5em">Ischaemic Heart Disease or Vascular Disease</span></p><hr>
                <p>Patients with documented coronary artery disease, in the absence of specific contraindications or documented intolerance, should be treated with anti-platelet agents; for patients with a history of chronic stable angina, remote PCI, or CABG, ASA (75 mg PO to 162 mg) PO daily indefinitely
    Cardiac rehabilitation programs and services are recommended for most, and potentially all, patients with documented cardiovascular disease.</p>

    <p>Patients diagnosed with peripheral vascular disease, carotid disease and AAA, require risk factor modification as they are at increased risk for CV events. </p>
    <p>Pharmacotherapy for those patients with vascular disease is like those with CAD so statins, an ACE inhibitor or ARB is recommended for most patients with hypertension and coronary artery disease.</p>
    <p>In patients with coronary artery disease and deemed to be at high risk, when combination therapy is being used, choices should be individualized. The combination of an ACE inhibitor and a dihydropyridine CCB is preferable to an ACE inhibitor and a diuretic in selected patients.</p>

    <p>For patients with stable angina, β-blockers are preferred as initial therapy. CCBs may also be used.</p>
    <p>For patients with recent myocardial infarction, initial therapy should include both a β-blocker and an ACE inhibitor. An ARB can be used if the patient is intolerant of an ACE inhibitor</p>
